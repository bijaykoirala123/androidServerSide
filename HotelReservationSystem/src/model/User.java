package model;

public class User {
	private int guest_id;
	private String guest_name;
	private String guest_email;
	private String guest_phone_number;
	private String guest_location;
	private String guest_gender;
	private String guest_password;
	
	public int getGuest_id() {
		return guest_id;
	}
	public void setGuest_id(int guest_id) {
		this.guest_id = guest_id;
	}
	public String getGuest_name() {
		return guest_name;
	}
	public void setGuest_name(String guest_name) {
		this.guest_name = guest_name;
	}
	public String getGuest_email() {
		return guest_email;
	}
	public void setGuest_email(String guest_email) {
		this.guest_email = guest_email;
	}
	public String getGuest_phone_number() {
		return guest_phone_number;
	}
	public void setGuest_phone_number(String guest_phone_number) {
		this.guest_phone_number = guest_phone_number;
	}
	public String getGuest_location() {
		return guest_location;
	}
	public void setGuest_location(String guest_location) {
		this.guest_location = guest_location;
	}
	public String getGuest_gender() {
		return guest_gender;
	}
	public void setGuest_gender(String guest_gender) {
		this.guest_gender = guest_gender;
	}
	public String getGuest_password() {
		return guest_password;
	}
	public void setGuest_password(String guest_password) {
		this.guest_password = guest_password;
	}
	
	

}
